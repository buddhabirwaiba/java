package array;
public class multiarray {
    public static void main(String[] args){
        int[][] roll ={{1,2},{3,4},{5,6}};
        System.out.println(roll);
        for(int i=0;i<roll.length;i++){
            for(int j=0;j<roll.length;j++){
            System.out.println(roll[i][j]);
            System.out.println("");
        } 
        System.out.println("");
        }
    }
}
